package smtk.cf.a0_listview_ex;

public class Item {
    private String color;
    private String text;

    public Item(){

    }

    public void setColor(String color) {
        this.color = color;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getColor() {
        return color;
    }

    public String getText() {
        return text;
    }
}
